// Helper functions
export const getProductBySlug = (slug: string): Product | undefined => {
  return products.find((p) => p.slug === slug);
};

export const getProductById = (id: string): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getProductsByCategory = (categorySlug: string): Product[] => {
  return products.filter((p) => p.category.slug === categorySlug);
};

export const getRelatedProducts = (productId: string, limit: number = 4): Product[] => {
  const product = getProductById(productId);
  if (!product) return [];
  
  return products
    .filter((p) => p.id !== productId && p.category.id === product.category.id)
    .slice(0, limit);
};

export const getBestsellers = (limit: number = 6): Product[] => {
  return products.filter((p) => p.isBestseller).slice(0, limit);
};

export const getNewArrivals = (limit: number = 6): Product[] => {
  return products.filter((p) => p.isNew).slice(0, limit);
};

export const getOnSale = (limit: number = 6): Product[] => {
  return products.filter((p) => p.isOnSale).slice(0, limit);
};

export const searchProducts = (query: string): Product[] => {
  const lowercaseQuery = query.toLowerCase();
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(lowercaseQuery) ||
      p.description.toLowerCase().includes(lowercaseQuery) ||
      p.category.name.toLowerCase().includes(lowercaseQuery)
  );
};
